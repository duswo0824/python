# 콜라, 생수, 오렌지주스, 식혜, 이온음료
item = '커피' # = : 변수값을 저장할때 사용

if item == '콜라' : # == (비교연산자) : 두 값을 비교
    print(f'{item} 가 나왔습니다.')
    
elif item == '생수' : #else if = elif
    print(f'{item} 가 나왔습니다.')
    
elif item == '오렌지주스' :
    print(f'{item} 가 나왔습니다.')
    
elif item == '식혜' :
    print(f'{item} 가 나왔습니다.')
    
elif item == '이온음료' :
    print(f'{item} 가 나왔습니다.')

else:
    print(f'{item} 는 보유하지 않은 음료입니다.')


    